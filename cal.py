def add_numbers(a, b):
    """Function to add two numbers."""
    return a + b

if __name__ == "__main__":
    # Define two numbers to add
    num1 = 5
    num2 = 3
    
    # Perform the addition
    result = add_numbers(num1, num2)
    
    # Print the result
    print(f"The sum of {num1} and {num2} is {result}")

